// Helper function to get all videos across categories
export function getAllVideos(): WatchVideo[] {
  return watchCategories.flatMap(cat => cat.videos);
}

// Helper function to get category by ID
export function getCategoryById(id: string): WatchCategory | undefined {
  return watchCategories.find(cat => cat.id === id);
}

// Check if URL is a YouTube embed
export function isYouTubeUrl(url: string): boolean {
  return url.includes('youtube.com/embed');
}
